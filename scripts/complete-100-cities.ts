// Main function to complete the 100-city dataset
async function complete100Cities() {
  try {
    console.log("ğŸ¯ Completing 100-city dataset...");

    // Check current count
    const currentCount = await prisma.city.count();
    console.log(`ğŸ“Š Current cities in database: ${currentCount}`);

    if (currentCount >= 100) {
      console.log("âœ… Database already has 100+ cities!");
      return;
    }

    // Calculate how many more cities we need
    const needed = 100 - currentCount;
    const citiesToAdd = remainingCities.slice(0, needed);

    console.log(`ğŸ“ Adding ${citiesToAdd.length} cities to reach 100...`);

    // Insert in batches
    const batchSize = 10;
    let inserted = 0;

    for (let i = 0; i < citiesToAdd.length; i += batchSize) {
      const batch = citiesToAdd.slice(i, i + batchSize);

      await prisma.city.createMany({
        data: batch,
        skipDuplicates: true,
      });

      inserted += batch.length;
      console.log(`âœ… Inserted ${inserted}/${citiesToAdd.length} cities`);
    }

    // Final verification
    const finalCount = await prisma.city.count();
    console.log(`ğŸ‰ Dataset completed! Total cities: ${finalCount}`);

    // Statistics
    const featured = await prisma.city.count({ where: { featured: true } });
    const verified = await prisma.city.count({ where: { verified: true } });
    const countries = await prisma.city.groupBy({
      by: ["country"],
      _count: { country: true },
    });

    console.log(`â­ Featured cities: ${featured}`);
    console.log(`âœ… Verified cities: ${verified}`);
    console.log(`ğŸŒ Countries represented: ${countries.length}`);
  } catch (error) {
    console.error("âŒ Error completing dataset:", error);
    throw error;
  }
}

// Main execution
async function main() {
  try {
    await complete100Cities();
    console.log("ğŸ‰ 100-city dataset completion successful!");
  } catch (error) {
    console.error("ğŸ’¥ Failed to complete dataset:", error);
    process.exit(1);
  } finally {
    await prisma.$disconnect();
  }
}

