async function addFinal33Cities() {
  try {
    console.log("ğŸ¯ Adding final 33 cities to reach exactly 100...");

    const currentCount = await prisma.city.count();
    console.log(`ğŸ“Š Current cities: ${currentCount}`);

    if (currentCount >= 100) {
      console.log("âœ… Already have 100+ cities!");
      return;
    }

    const needed = 100 - currentCount;
    const citiesToAdd = final33Cities.slice(0, needed);

    console.log(`ğŸ“ Adding ${citiesToAdd.length} cities...`);

    // Insert in batches
    const batchSize = 10;
    let inserted = 0;

    for (let i = 0; i < citiesToAdd.length; i += batchSize) {
      const batch = citiesToAdd.slice(i, i + batchSize);

      await prisma.city.createMany({
        data: batch,
        skipDuplicates: true,
      });

      inserted += batch.length;
      console.log(`âœ… Inserted ${inserted}/${citiesToAdd.length} cities`);
    }

    const finalCount = await prisma.city.count();
    console.log(`ğŸ‰ Final count: ${finalCount} cities!`);

    if (finalCount >= 100) {
      console.log("ğŸ¯ TARGET ACHIEVED: 100+ cities in database!");
    }
  } catch (error) {
    console.error("âŒ Error:", error);
    throw error;
  }
}

